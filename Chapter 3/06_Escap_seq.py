print("Turn dreams into code, one bug at a time.\n Keep going, success is near.") 
# \t - Tab
print("Learning\tPython is fun!\n\tLet's keep coding!")  
# \\ - Backslash
print("C:\\User\\Ikrash")       
# single quote - \'
print('It\'s a beautiful day!')
# double quote - \"
print("I Love ‚ù§  \"Python\" Programming")
# \r - Carriage return
print("Hello\rWorld \"Because the world has overwritten \"Hello\" \" ")  
# \b - Backspace
print("Hellow\b World")