# 10 Rules for Naming Variables in Python
# In Python, variable names must follow certain rules to ensure they are valid and meaningful.
# Here are the 10 rules for naming variables in Python:

# 1. Variable names must start with a letter or underscore (_).
# 2. Variable names can contain letters, numbers, and underscores.
# 3. Variable names are case-sensitive (e.g., `myVar` and `myvar` are different).
# 4. Variable names cannot be a Python keyword (e.g., `if`, `else`, `while`, etc.).
# 5. Variable names should be descriptive and meaningful.
# 6. Avoid using special characters (e.g., @, #, $, %, etc.) in variable names.
# 7. Variable names should not start with a number.
# 8. Use underscores to separate words in variable names (e.g., `my_variable`).
# 9. Keep variable names concise but clear.
# 10. Avoid using single-character variable names except in small scopes (e.g., loops).


